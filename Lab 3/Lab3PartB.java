		//key size in bytes - four word = 16 byte
		int keySize = 16;
		byte[] keyBytes = new byte[keySize]; //key size stored as byte array
		
		//end word size in bytes - 44 words = 176 bytes
		int wordKeySize = 44 * 4;
		byte[] wordKeyBytes = new byte[wordKeySize]; //end word stored as byte array
		
		//convert the key string into a byte array
